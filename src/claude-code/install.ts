import { homedir } from 'node:os';
import { join } from 'node:path';
import { installFiles } from '../install/file-writer.js';
import { escapeJsonString, promptCaptureHookScript } from '../install/prompt-hook.js';
import type { InstallFile, InstallOptions, InstallResult } from '../install/types.js';

export async function installClaudeCode(options: InstallOptions = {}): Promise<InstallResult> {
  return installFiles('claude-code', claudeCodeFiles(options.homeDir ?? homedir(), options.includeHooks), options);
}

export function claudeCodeFiles(homeDir = homedir(), includeHooks = false): InstallFile[] {
  const files: InstallFile[] = [
    {
      path: join(homeDir, '.claude', 'skills', 'contextbook', 'SKILL.md'),
      description: 'Claude Code personal skill for Contextbook learning workflows',
      content: claudeSkillContent()
    },
    {
      path: join(homeDir, '.claude', 'commands', 'contextbook-learn.md'),
      description: 'Claude Code slash command compatibility file for /contextbook-learn',
      content: claudeLearnCommandContent()
    },
    {
      path: join(homeDir, '.claude', 'commands', 'contextbook-why.md'),
      description: 'Claude Code slash command compatibility file for /contextbook-why',
      content: claudeWhyCommandContent()
    }
  ];

  if (includeHooks) files.push(...claudeHookFiles(homeDir));
  return files;
}

function claudeHookFiles(homeDir: string): InstallFile[] {
  const scriptPath = join(homeDir, '.claude', 'hooks', 'contextbook-user-prompt-submit.js');
  const guidePath = join(homeDir, '.claude', 'hooks', 'contextbook-user-prompt-submit.md');
  return [
    {
      path: scriptPath,
      description: 'Claude Code UserPromptSubmit hook helper for deterministic Contextbook prompt signal capture',
      content: promptCaptureHookScript('claude-code')
    },
    {
      path: guidePath,
      description: 'Claude Code hook configuration snippet for enabling Contextbook prompt signal capture',
      content: claudeHookGuide(scriptPath)
    }
  ];
}

function claudeHookGuide(scriptPath: string): string {
  return `# Contextbook Claude Code UserPromptSubmit Hook

This file was generated by \`contextbook setup --hooks\`.

Contextbook does **not** edit your Claude Code settings automatically. To enable prompt signal capture, merge this snippet into \`~/.claude/settings.json\` or a project-scoped Claude settings file:

\`\`\`json
{
  "hooks": {
    "UserPromptSubmit": [
      {
        "hooks": [
          {
            "type": "command",
            "command": "node \\\"${escapeJsonString(scriptPath)}\\\"",
            "timeout": 30,
            "statusMessage": "Capturing Contextbook learning signal"
          }
        ]
      }
    ]
  }
}
\`\`\`

## Safety

- The hook calls \`contextbook memory capture-prompt --source claude-code --json\`.
- Contextbook classifies only explicit learning feedback signals.
- Contextbook does not persist the raw prompt text.
- The hook exits successfully even if capture fails, so it should not block Claude Code.
`;
}

function claudeSkillContent(): string {
  return `---
name: contextbook
description: Use Contextbook to explain the development and CS concepts behind code the user just touched, grounded in local project evidence.
---

# Contextbook

Use the local \`contextbook\` CLI for project-grounded learning moments and why-answers.

## Runbook

- If the project has not been initialized, run \`contextbook init\`.
- Refresh evidence with \`contextbook scan\` when code changed or memory may be stale.
- Use \`contextbook memory context --json\` as the default one-shot AI context bundle across Project, Learner, Conversation Memory, suggestions, freshness, and safety.
- Use \`contextbook project --json\` or \`contextbook learner --json\` only when debugging a specific memory layer.
- Use \`contextbook memory add-signal --type <type>\` only for explicit user feedback; never infer ability or mutate profile.
- Use \`contextbook memory capture-prompt --prompt "<user prompt>" --source claude-code --json\` for hook-ready explicit prompt feedback capture without storing raw transcript text.
- Use \`contextbook memory suggest-weak-terms --json\` to inspect review candidates without mutating weak terms.
- Use \`contextbook memory suggest-profile-updates --json\` to inspect profile update candidates without editing profile/preferences.
- Use \`contextbook memory apply-profile-update --candidate <id|index> --dry-run\` to preview exact preferences-only changes; apply without \`--dry-run\` only after explicit user approval.
- Use \`contextbook memory apply-preference-signals --prompt "<user prompt>" --source claude-code --dry-run\` to preview allowlisted language/order/length/command-volume preference writes without storing the raw prompt.
- Use \`contextbook learn\` to produce learning moments from the current project/diff.
- Use \`contextbook why "$ARGUMENTS"\` for concept questions when invoked with arguments.
- Keep the CLI's evidence level visible: \`direct\`, \`related\`, or \`general\`.
- Do not add external LLM/API setup; Contextbook v0.1 works locally.

## Output rule

When summarizing Contextbook output, preserve project files, evidence level, and the interview sentence. Do not invent project evidence that is not present in the CLI output.
`;
}

function claudeLearnCommandContent(): string {
  return `---
description: Generate Contextbook learning moments from the current repository.
---

Run Contextbook locally and report the result without inventing extra evidence:

1. Run \`contextbook scan\` if project evidence may be stale.
2. Run \`contextbook memory context --json\` for the one-shot AI context bundle before summarizing.
3. Run lower-level \`contextbook project --json\`, \`contextbook learner --json\`, or suggestion commands only when debugging a specific layer.
4. If a profile update candidate matters, preview it with \`contextbook memory apply-profile-update --candidate <id|index> --dry-run\` and wait for explicit user approval before applying.
5. If the user explicitly states a safe preference, preview \`contextbook memory apply-preference-signals --prompt "$ARGUMENTS" --source claude-code --dry-run\`; do not auto-apply from hooks.
6. Run \`contextbook learn\`.
7. Preserve the evidence level and evidence files from the output.
`;
}

function claudeWhyCommandContent(): string {
  return `---
description: Answer a concept question with Contextbook project evidence.
---

Answer this question using Contextbook:

$ARGUMENTS

Run:

\`\`\`bash
contextbook why "$ARGUMENTS"
\`\`\`

Preserve the evidence level, project-language explanation, CS connection, interview sentence, and evidence files. If Contextbook says evidence is \`general\`, do not imply the concept was found directly in the project.
`;
}
